package Controle;

public class Switch {

    public static void main(String[] args) {

        int idade = 3;

        switch (idade) {

            case 3:
                System.out.println("sabe andar");
            case 2:
                System.out.println("saber falar");
            case 1:
                System.out.println("sabe engatinhar");
            default:
                System.out.println("Respira");
        }

    }

}
